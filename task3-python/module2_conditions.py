# Запрашиваем прибыль за месяц
profit = float(input("Введите прибыль за месяц (руб.): "))

# Условия: проверяем результат
if profit > 0:
    print("Результат: ПРИБЫЛЬ")
elif profit < 0:
    print("Результат: УБЫТОК")
else:
    print("Результат: БЕЗУБЫТОЧНОСТЬ")

# =====================================================
# Упражнение 3. Расчёт НДФЛ (налог на доходы)
# =====================================================

print("\n" + "="*50)
print("Упражнение 3. Расчёт НДФЛ")
print("="*50)

# Запрашиваем зарплату
salary = float(input("Введите зарплату (руб.): "))

# Условие: ставка зависит от суммы
if salary <= 50000:
    tax_rate = 13  # 13%
else:
    tax_rate = 15  # 15%

# Вычисляем налог и сумму "на руки"
tax = salary * (tax_rate / 100)
net_salary = salary - tax

# Выводим результаты
print(f"Ставка налога: {tax_rate}%")
print(f"Сумма налога: {tax:.2f} руб.")
print(f"Зарплата 'на руки': {net_salary:.2f} руб.")

# =====================================================
# Упражнение 4. Таблица умножения ставки
# =====================================================

print("\n" + "="*50)
print("Упражнение 4. Таблица умножения ставки")
print("="*50)

# Запрашиваем процентную ставку
rate = float(input("Введите процентную ставку (%): "))

# Цикл for: выводим умножение от 1 до 12
print(f"\nТаблица умножения ставки {rate}%:")
for i in range(1, 13):
    result = rate * i
    print(f"{rate} × {i} = {result}")

# =====================================================
# Упражнение 5. Анализ цен (дорого/не дорого)
# =====================================================

print("\n" + "="*50)
print("Упражнение 5. Анализ цен")
print("="*50)

# Список из 5 цен
prices = [129.99, 450.00, 89.50, 1200.00, 299.99]

print("Список цен:", prices)
print("\nАнализ (дорого > 300 руб.):")

# Цикл for: проходим по каждой цене
for price in prices:
    if price > 300:
        print(f"{price:.2f} руб. - ДОРОГО")
    else:
        print(f"{price:.2f} руб. - нормально")

# =====================================================
# Упражнение 9. Выручка за полгода (min, max, среднее)
# =====================================================

print("\n" + "="*50)
print("Упражнение 9. Выручка за полгода")
print("="*50)

# Список для хранения выручки
revenues = []

# Цикл for: вводим выручку за 6 месяцев
for month in range(1, 7):
    revenue = float(input(f"Введите выручку за месяц {month}: "))
    revenues.append(revenue)  # добавляем в список

# Находим минимум, максимум и среднее
min_revenue = min(revenues)
max_revenue = max(revenues)
avg_revenue = sum(revenues) / len(revenues)

# Выводим результаты
print("\n=== Анализ выручки за полугодие ===")
print(f"Минимальная выручка: {min_revenue:.2f} руб.")
print(f"Максимальная выручка: {max_revenue:.2f} руб.")
print(f"Средняя выручка: {avg_revenue:.2f} руб.")

print("\n" + "="*50)
print("Все 5 упражнений выполнены!")
print("="*50)
