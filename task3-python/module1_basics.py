# Упражнение 6. Прибыль и рентабельность
# =====================================================

print("\n" + "="*50)
print("Упражнение 6. Прибыль и рентабельность")
print("="*50)

# Запрашиваем выручку
revenue = float(input("Введите выручку (руб.): "))

# Запрашиваем затраты
costs = float(input("Введите затраты (руб.): "))

# Вычисляем прибыль
profit = revenue - costs

# Вычисляем рентабельность (прибыль / выручка * 100%)
if revenue > 0:
    profitability = (profit / revenue) * 100
else:
    profitability = 0

# Выводим результаты
print(f"Прибыль: {profit:.2f} руб.")
print(f"Рентабельность: {profitability:.2f}%")


