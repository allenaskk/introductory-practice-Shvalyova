# Запрашиваем количество товара
quantity_goods = int(input("Введите количество товара: "))

# Запрашиваем цену за единицу
price_per_unit = float(input("Введите цену за единицу товара: "))

# Запрашиваем ставку НДС (в процентах)
vat_rate = float(input("Введите ставку НДС (в %): "))

# Вычисляем стоимость без НДС
cost_without_vat = quantity_goods * price_per_unit

# Вычисляем сумму НДС
vat_amount = cost_without_vat * (vat_rate / 100)

# Вычисляем итоговую стоимость с НДС
total_with_vat = cost_without_vat + vat_amount

# Выводим результаты
print(f"Стоимость без НДС: {cost_without_vat:.2f} руб.")
print(f"Сумма НДС ({vat_rate}%): {vat_amount:.2f} руб.")
print(f"Итого с НДС: {total_with_vat:.2f} руб.")


