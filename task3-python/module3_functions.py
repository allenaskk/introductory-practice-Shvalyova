def calculate_vat(price, vat_rate=20):
    """
    Функция принимает цену и ставку НДС (по умолчанию 20%),
    возвращает сумму налога.
    """
    vat_amount = price * (vat_rate / 100)
    return vat_amount

# Вызов с явной ставкой (20%)
print("Товар ценой 1000 руб., ставка НДС 20%")
vat1 = calculate_vat(1000, 20)
print(f"Сумма НДС: {vat1:.2f} руб.")

# Вызов со стандартной ставкой (по умолчанию 20%)
print("\nТовар ценой 5000 руб., ставка НДС по умолчанию (20%)")
vat2 = calculate_vat(5000)
print(f"Сумма НДС: {vat2:.2f} руб.")

# Вызов с другой ставкой (10%)
print("\nТовар ценой 3000 руб., ставка НДС 10%")
vat3 = calculate_vat(3000, 10)
print(f"Сумма НДС: {vat3:.2f} руб.")


