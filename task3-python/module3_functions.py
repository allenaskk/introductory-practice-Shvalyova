def apply_discount(price, discount_percent):
    """
    Функция принимает цену и процент скидки,
    возвращает новую цену со скидкой.
    """
    new_price = price * (1 - discount_percent / 100)
    return new_price

# Список из 5 товаров (название, цена)
products = [
    ("Ноутбук", 50000),
    ("Мышь", 1500),
    ("Клавиатура", 3000),
    ("Монитор", 20000),
    ("Наушники", 4000)
]

# Применяем скидку 15% ко всем товарам
discount = 15
print(f"Скидка {discount}% на все товары:\n")

for product_name, price in products:
    new_price = apply_discount(price, discount)
    saved = price - new_price
    print(f"{product_name}: {price:.2f} руб. → {new_price:.2f} руб. (экономия {saved:.2f} руб.)")


