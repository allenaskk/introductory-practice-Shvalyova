def generate_report(company_name, revenue, costs):
    """
    Функция принимает название компании, выручку и затраты,
    выводит многострочный отчёт: прибыль, рентабельность, вывод.
    """
    profit = revenue - costs
    if revenue > 0:
        profitability = (profit / revenue) * 100
    else:
        profitability = 0
    
    # Выводим отчёт
    print("=" * 40)
    print(f"ОТЧЁТ ПО КОМПАНИИ: {company_name}")
    print("=" * 40)
    print(f"Выручка: {revenue:,.2f} руб.")
    print(f"Затраты: {costs:,.2f} руб.")
    print(f"Прибыль: {profit:,.2f} руб.")
    print(f"Рентабельность: {profitability:.1f}%")
    
    # Вывод о состоянии
    if profit > 0:
        print("▶ Вывод: КОМПАНИЯ ПРИБЫЛЬНА")
    elif profit < 0:
        print("▶ Вывод: КОМПАНИЯ УБЫТОЧНА")
    else:
        print("▶ Вывод: БЕЗУБЫТОЧНОСТЬ")
    print("=" * 40)

