def get_category(revenue):
    """
    Функция возвращает категорию бизнеса по выручке.
    Микробизнес: до 1 млн
    Малый: до 10 млн
    Средний: до 100 млн
    Крупный: выше 100 млн
    """
    if revenue < 1_000_000:
        return "Микробизнес"
    elif revenue < 10_000_000:
        return "Малый"
    elif revenue < 100_000_000:
        return "Средний"
    else:
        return "Крупный"

# Тестируем на 4 значениях
test_revenues = [500_000, 5_000_000, 50_000_000, 200_000_000]

for rev in test_revenues:
    category = get_category(rev)
    print(f"Выручка {rev:,} руб. → Категория: {category}")


