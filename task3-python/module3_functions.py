def calculate_profit(revenue, costs):
    """
    Функция принимает выручку и затраты, возвращает прибыль.
    """
    profit = revenue - costs
    return profit

# Вызываем функцию с тремя разными парами значений
print("Вызов 1: выручка=100000, затраты=70000")
profit1 = calculate_profit(100000, 70000)
